-- ====================
-- Cart-change invalidation triggers
-- ====================
-- The BEFORE trigger reacts only to fields represented by the snapshot. It
-- takes the same parent-cart lock used by QR creation before the item mutation
-- becomes visible, so creation sees either the complete old or complete new
-- cart and a waiting edit always cancels the earlier snapshot.
CREATE OR REPLACE FUNCTION public.cancel_active_qr_after_cart_item_change()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = pg_catalog, public, extensions, auth
AS $function$
DECLARE
  v_old_cart_id UUID;
  v_new_cart_id UUID;
BEGIN
  IF TG_OP = 'UPDATE'
     AND OLD.cart_id IS NOT DISTINCT FROM NEW.cart_id
     AND OLD.shop_product_id IS NOT DISTINCT FROM NEW.shop_product_id
     AND OLD.quantity IS NOT DISTINCT FROM NEW.quantity THEN
    RETURN NEW;
  END IF;

  IF TG_OP IN ('UPDATE', 'DELETE') THEN
    v_old_cart_id := OLD.cart_id;
  END IF;

  IF TG_OP IN ('INSERT', 'UPDATE') THEN
    v_new_cart_id := NEW.cart_id;
  END IF;

  PERFORM c.id
  FROM public.carts AS c
  WHERE c.id = ANY (
    array_remove(ARRAY[v_old_cart_id, v_new_cart_id]::UUID[], NULL)
  )
  ORDER BY c.id
  FOR UPDATE;

  IF EXISTS (
    SELECT 1
    FROM public.carts AS c
    WHERE c.id = ANY (
      array_remove(ARRAY[v_old_cart_id, v_new_cart_id]::UUID[], NULL)
    )
      AND c.status <> 'active'
  ) THEN
    RAISE EXCEPTION 'Cart is no longer active'
      USING ERRCODE = '55000';
  END IF;

  UPDATE public.qr_sessions AS qs
  SET status = 'cancelled'
  WHERE qs.status = 'active'
    AND qs.cart_id = ANY (
      array_remove(ARRAY[v_old_cart_id, v_new_cart_id]::UUID[], NULL)
    );

  IF TG_OP = 'DELETE' THEN
    RETURN OLD;
  END IF;

  RETURN NEW;
END;
$function$;

REVOKE ALL ON FUNCTION public.cancel_active_qr_after_cart_item_change()
  FROM PUBLIC, anon, authenticated;

DROP TRIGGER IF EXISTS trg_cancel_active_qr_after_cart_item_change
  ON public.cart_items_v2;

CREATE TRIGGER trg_cancel_active_qr_after_cart_item_change
  BEFORE INSERT OR UPDATE OR DELETE ON public.cart_items_v2
  FOR EACH ROW
  EXECUTE FUNCTION public.cancel_active_qr_after_cart_item_change();

CREATE OR REPLACE FUNCTION public.cancel_active_qr_after_cart_change()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = pg_catalog, public, extensions, auth
AS $function$
BEGIN
  IF OLD.user_id IS NOT DISTINCT FROM NEW.user_id
     AND OLD.shop_id IS NOT DISTINCT FROM NEW.shop_id
     AND OLD.status IS NOT DISTINCT FROM NEW.status THEN
    RETURN NEW;
  END IF;

  UPDATE public.qr_sessions AS qs
  SET status = 'cancelled'
  WHERE qs.cart_id = NEW.id
    AND qs.status = 'active';

  RETURN NEW;
END;
$function$;

REVOKE ALL ON FUNCTION public.cancel_active_qr_after_cart_change()
  FROM PUBLIC, anon, authenticated;

DROP TRIGGER IF EXISTS trg_cancel_active_qr_after_cart_change
  ON public.carts;

CREATE TRIGGER trg_cancel_active_qr_after_cart_change
  AFTER UPDATE OF user_id, shop_id, status ON public.carts
  FOR EACH ROW
  EXECUTE FUNCTION public.cancel_active_qr_after_cart_change();

-- ====================
-- Customer RPC: create a locked, immutable QR snapshot
-- ====================
CREATE OR REPLACE FUNCTION public.create_qr_session(p_cart_id UUID)
RETURNS public.qr_sessions
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = pg_catalog, public, extensions, auth
AS $function$
DECLARE
  v_user_id UUID;
  v_now TIMESTAMPTZ := clock_timestamp();
  v_cart public.carts%ROWTYPE;
  v_session public.qr_sessions%ROWTYPE;
  v_cart_line_count INTEGER;
  v_snapshot_line_count INTEGER;
  v_snapshot_item_count INTEGER;
  v_snapshot_total NUMERIC;
BEGIN
  v_user_id := auth.uid();

  IF v_user_id IS NULL THEN
    RAISE EXCEPTION 'Authentication required'
      USING ERRCODE = '28000';
  END IF;

  -- This row lock is the serialization point shared with cart-change triggers
  -- and confirmation. Concurrent create calls for one cart cannot diverge.
  SELECT c.*
    INTO v_cart
  FROM public.carts AS c
  WHERE c.id = p_cart_id
    AND c.user_id = v_user_id
    AND c.status = 'active'
  FOR UPDATE;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Active cart not found'
      USING ERRCODE = 'P0002';
  END IF;

  IF NOT EXISTS (
    SELECT 1
    FROM public.shops AS s
    WHERE s.id = v_cart.shop_id
      AND s.is_active = TRUE
  ) THEN
    RAISE EXCEPTION 'Shop is not active'
      USING ERRCODE = '55000';
  END IF;

  -- Every cart-item mutation takes this same parent-cart lock in its BEFORE
  -- trigger. The following reads therefore describe one stable cart version.
  SELECT COUNT(*)::INTEGER
    INTO v_cart_line_count
  FROM public.cart_items_v2 AS ci
  WHERE ci.cart_id = v_cart.id;

  IF v_cart_line_count = 0 THEN
    RAISE EXCEPTION 'Cannot create QR session for an empty cart'
      USING ERRCODE = 'P0001';
  END IF;

  IF EXISTS (
    SELECT 1
    FROM public.cart_items_v2 AS ci
    JOIN public.shop_products AS sp ON sp.id = ci.shop_product_id
    WHERE ci.cart_id = v_cart.id
      AND (
        sp.shop_id IS DISTINCT FROM v_cart.shop_id
        OR sp.is_active IS NOT TRUE
        OR sp.is_available IS NOT TRUE
        OR sp.price IS NULL
        OR sp.price < 0
      )
  ) THEN
    RAISE EXCEPTION 'Cart contains an unavailable or different shop item'
      USING ERRCODE = '23514';
  END IF;

  UPDATE public.qr_sessions AS qs
  SET status = 'expired'
  WHERE qs.cart_id = v_cart.id
    AND qs.user_id = v_user_id
    AND qs.status = 'active'
    AND qs.expires_at <= v_now;

  SELECT qs.*
    INTO v_session
  FROM public.qr_sessions AS qs
  WHERE qs.cart_id = v_cart.id
    AND qs.user_id = v_user_id
    AND qs.status = 'active'
    AND qs.expires_at > v_now
  LIMIT 1
  FOR UPDATE;

  IF FOUND THEN
    SELECT
      COUNT(*)::INTEGER,
      COALESCE(SUM(qsi.quantity), 0)::INTEGER,
      COALESCE(SUM(qsi.line_total), 0)
      INTO
        v_snapshot_line_count,
        v_snapshot_item_count,
        v_snapshot_total
    FROM public.qr_session_items AS qsi
    WHERE qsi.qr_session_id = v_session.id;

    IF v_snapshot_line_count > 0
       AND v_session.item_count = v_snapshot_item_count
       AND v_session.total_amount = v_snapshot_total THEN
      RETURN v_session;
    END IF;

    -- Never reuse an incomplete or internally inconsistent active snapshot.
    UPDATE public.qr_sessions
    SET status = 'cancelled'
    WHERE id = v_session.id;
  END IF;

  INSERT INTO public.qr_sessions (
    session_token,
    user_id,
    cart_id,
    shop_id,
    status,
    expires_at,
    used_at,
    total_amount,
    item_count,
    confirmed_by_user_id
  )
  VALUES (
    encode(gen_random_bytes(32), 'hex'),
    v_user_id,
    v_cart.id,
    v_cart.shop_id,
    'active',
    v_now + interval '2 minutes',
    NULL,
    0,
    0,
    NULL
  )
  RETURNING * INTO v_session;

  INSERT INTO public.qr_session_items (
    qr_session_id,
    shop_product_id,
    product_name,
    quantity,
    unit_price
  )
  SELECT
    v_session.id,
    sp.id,
    p.name,
    ci.quantity,
    sp.price
  FROM public.cart_items_v2 AS ci
  JOIN public.shop_products AS sp
    ON sp.id = ci.shop_product_id
   AND sp.shop_id = v_cart.shop_id
  JOIN public.products AS p ON p.id = sp.product_id
  WHERE ci.cart_id = v_cart.id
  ORDER BY ci.id;

  GET DIAGNOSTICS v_snapshot_line_count = ROW_COUNT;

  IF v_snapshot_line_count <> v_cart_line_count THEN
    RAISE EXCEPTION
      'Cart snapshot is incomplete (% of % lines copied)',
      v_snapshot_line_count,
      v_cart_line_count
      USING ERRCODE = 'P0001';
  END IF;

  SELECT
    COALESCE(SUM(qsi.quantity), 0)::INTEGER,
    COALESCE(SUM(qsi.line_total), 0)
    INTO v_snapshot_item_count, v_snapshot_total
  FROM public.qr_session_items AS qsi
  WHERE qsi.qr_session_id = v_session.id;

  UPDATE public.qr_sessions
  SET item_count = v_snapshot_item_count,
      total_amount = v_snapshot_total
  WHERE id = v_session.id
  RETURNING * INTO v_session;

  RETURN v_session;
END;
$function$;

