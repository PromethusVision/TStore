-- =============================================================
-- Esnafta Var - QR snapshot and merchant verification migration
-- =============================================================
-- Purpose:
-- - Freeze product name, quantity, and price when a customer creates a QR.
-- - Keep at most one active QR per cart.
-- - Cancel the active QR whenever its cart contents meaningfully change.
-- - Allow only the merchant/admin owner of the matching shop to inspect and
--   confirm a QR through narrowly scoped SECURITY DEFINER RPC functions.
-- - Complete a confirmation exactly once and check out the cart atomically.
--
-- Safety:
-- - This migration adds snapshot data and hardens existing cart write rules;
--   it is safe to re-run.
-- - It does not DROP/TRUNCATE any table or delete existing rows.
-- - Existing active QR rows without a snapshot are marked cancelled because
--   their historical price and contents cannot be verified safely.
-- - Lock acquisition and individual statements have bounded wait times; any
--   timeout aborts this transaction before a partial migration can commit.
-- - Run this file only after the profiles role, shops/shop_products, carts v2,
--   and qr_sessions migrations have completed.
-- =============================================================

