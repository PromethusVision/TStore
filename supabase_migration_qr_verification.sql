DECLARE
  v_constraint_name TEXT;
BEGIN
  FOR v_constraint_name IN
    SELECT c.conname
    FROM pg_constraint AS c
    JOIN pg_attribute AS a
      ON a.attrelid = c.conrelid
     AND a.attnum = ANY (c.conkey)
    WHERE c.contype = 'f'
      AND c.conrelid = 'public.cart_items_v2'::regclass
      AND c.confrelid = 'public.shop_products'::regclass
      AND a.attname = 'shop_product_id'
  LOOP
    EXECUTE format(
      'ALTER TABLE public.cart_items_v2 DROP CONSTRAINT %I',
      v_constraint_name
    );
  END LOOP;

  ALTER TABLE public.cart_items_v2
    ADD CONSTRAINT cart_items_v2_shop_product_id_fkey
    FOREIGN KEY (shop_product_id)
    REFERENCES public.shop_products(id)
    ON DELETE RESTRICT;
END
