-- ====================
-- Cart state and write hardening
-- ====================
-- A customer may edit only an active cart and may transition it only to
-- cancelled. Only the trusted confirmation RPC may set checked_out. This
-- keeps a verified cart and its proof from being changed or deleted later.
DO $cart_status_constraint$
BEGIN
  IF NOT EXISTS (
    SELECT 1
    FROM pg_constraint
    WHERE conname = 'carts_status_check'
      AND conrelid = 'public.carts'::regclass
  ) THEN
    ALTER TABLE public.carts
      ADD CONSTRAINT carts_status_check
      CHECK (status IN ('active', 'checked_out', 'cancelled', 'expired'));
  END IF;
END
